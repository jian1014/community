package com.xiaojian.community.dto;

public class GithubUser {
    private String name;
    private Long id;
    private String tio;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTio() {
        return tio;
    }

    public void setTio(String tio) {
        this.tio = tio;
    }
}
